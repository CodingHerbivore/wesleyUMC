      <h3>A Thought on Pets in Church</h3>
      <p><strong>By Fred Shaw</strong><br /><span class="whois">Methodist Elder in the Ohio Conference, Director of Native American COS, and Shawnee Native</span></p>
      <p><img src="./images/wesley-pets0001.jpg" alt="One of Wesley UMC's visiting dogs during the pet blessing service" class="frontPageImg"/>One of the threats we face today is that we humans drastically have separated God and God's creation in our minds and practices. We compartmentalize life so that we can manipulate "things" for our own benefit (real and imagined) and ignore the sanctity of their own being. That enables us to believe we can exploit what God has created, that it is less sacred than our desires. That breaks the circle, the balance, of all being. We can take more than we give and feel that we are justified in doing so. Hence, the present practice of focusing upon the bottom line of profit-and-loss rather than maintaining a sustainable balance for the future.</p>
      <p>God&rsquo;s being encompasses all that is and all that is not, which makes all life sacred. The Sacred personified itself in birth in a stable. Jesus spent his vision quest of 40 days among the animals and flora in the wilderness. Jesus' teachings often used the animals and the plants in making his point. The Apostle Paul indicated that all of the creation is waiting for the redemption that is in the Christ.</p>
      <p>Will having pets in worship offset the imbalance of human practice? Should we turn the sanctuary into a barn? Of course not, but neither should we be concerned that God is offended by the presence of any of God's creation in worship. Maybe, just maybe, that small, dependent life that gives another person such unconditional joy and love will remind someone of how undeserving we are of the love of the Sacred, and that all being is sacred because God looked at it and said, &ldquo;It is good.&rdquo;</p>
